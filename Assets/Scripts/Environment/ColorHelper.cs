using System.Collections;
using System.Collections.Generic;
using UnityEngine;


public class ColorHSV
{
    public float hue;
    public float saturation;
    public float value;

    public ColorHSV(float hue, float saturation, float value)
    {
        this.hue = hue;
        this.saturation = saturation;
        this.value = value;
    }
}
